#[test]
fn test_proof_of_funds_passes_when_balance_meets_threshold() {
    let env = Env::default();
    let vk = proof_of_funds_vk(&env);

    // Proof for balance=5000 (private), threshold=1000 (public). circom
    // forces ok=1 to be the only satisfiable witness, so this proof
    // existing at all already proves balance >= threshold.
    let pi_ax = "730116210369296444092450637108423573637911240070279320470610952043483226338101207935111255754307564298846192677690";
    let pi_ay = "1276668180892540803963422962972801590104672294743413198304254599525545042264988240988544623688552396914235818597706";
    let pi_bx1 = "1129120045065648976359829509677878513474748385015597760841862959181514305075172286355293270779433775669174452788085";
    let pi_bx2 = "1731186703337052685119434723246951315502571896719446182084973953672722330439473230312429321716542809107666863818150";
    let pi_by1 = "1274719505103895854114261659839953209657514183538608334124395792094256125786834298892311208723724776679640275053857";
    let pi_by2 = "3500416923337641686041796981886104075878231311447212227669391788842955913794256084056533784101831415219618003611456";
    let pi_cx = "2353248261936925120050090097019627474931837366164321792165337453220941512321916253631527847431411048116172561500552";
    let pi_cy = "3624467196391487702568782517462395008668375571325102046691149459267599605363209101724105797299512522642187146487636";

    let proof = Proof {
        a: g1_from_coords(&env, pi_ax, pi_ay),
        b: g2_from_coords(&env, pi_bx1, pi_bx2, pi_by1, pi_by2),
        c: g1_from_coords(&env, pi_cx, pi_cy),
    };

    let client = create_client(&env);

    // snarkjs public.json order is [ok, threshold] = [1, 1000] — must
    // match the IC ordering in the verification key exactly.
    let pub_signals = Vec::from_array(
        &env,
        [
            Fr::from_u256(U256::from_u32(&env, 1)),
            Fr::from_u256(U256::from_u32(&env, 1000)),
        ],
    );
    let res = client.verify_proof(&vk, &proof, &pub_signals);
    assert_eq!(res, true);

    env.cost_estimate().budget().print();

    // Tampering with the claimed threshold after the fact must fail —
    // the proof was bound to threshold=1000, not 1.
    let tampered_signals = Vec::from_array(
        &env,
        [
            Fr::from_u256(U256::from_u32(&env, 1)),
            Fr::from_u256(U256::from_u32(&env, 1)),
        ],
    );
    let res = client.verify_proof(&vk, &proof, &tampered_signals);
    assert_eq!(res, false);
}

