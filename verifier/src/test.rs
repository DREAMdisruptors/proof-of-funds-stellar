#[test]
fn test_proof_of_funds_passes_when_balance_meets_threshold() {
    let env = Env::default();
    let vk = proof_of_funds_vk(&env);

    // Proof generated from circuits/input.json: balance=5000 (private),
    // threshold=1000 (public). circom forces ok=1 to be the only
    // satisfiable witness, so this proof existing at all already proves
    // balance >= threshold.
    let pi_ax = "753836167102140828414083473243010253749858814572544316626445456010829074784990927861940120861981043211260501154498";
    let pi_ay = "968625006575500017310474903058692042726957119689964090903757840115335262329976032052204821317819459890152188043274";
    let pi_bx1 = "2831110271995901798466584811791325373781433267601744625750200156539598810334674757105370672389567270088551145534985";
    let pi_bx2 = "56544606319286596131923807394807831142837738470345335163371246763283349796714932944043653741654008203893055460281";
    let pi_by1 = "2784688861018154782747418068292507468587542649279517793327670062488094063670813200291159193660842950981629917975504";
    let pi_by2 = "1547108409325021516253113285558425742566434310689874243751615652130158468906321060926287284090089653486845073886608";
    let pi_cx = "2769500033095692680127625994690521873602484515196747427400605263378520063042219161574241427372598053311412847561496";
    let pi_cy = "1296373748512332021635332612125728806191230533981149271942599028598781631831537517837168054199304472788610424283744";

    let proof = Proof {
        a: g1_from_coords(&env, pi_ax, pi_ay),
        b: g2_from_coords(&env, pi_bx1, pi_bx2, pi_by1, pi_by2),
        c: g1_from_coords(&env, pi_cx, pi_cy),
    };

    let client = create_client(&env);

    // snarkjs public.json order is [ok, threshold] = [1, 1000] — must
    // match the IC ordering in the verification key exactly.
    let pub_signals = Vec::from_array(
        &env,
        [
            Fr::from_u256(U256::from_u32(&env, 1)),
            Fr::from_u256(U256::from_u32(&env, 1000)),
        ],
    );
    let res = client.verify_proof(&vk, &proof, &pub_signals);
    assert_eq!(res, true);

    env.cost_estimate().budget().print();

    // Tampering with the claimed threshold after the fact must fail —
    // the proof was bound to threshold=1000, not 1.
    let tampered_signals = Vec::from_array(
        &env,
        [
            Fr::from_u256(U256::from_u32(&env, 1)),
            Fr::from_u256(U256::from_u32(&env, 1)),
        ],
    );
    let res = client.verify_proof(&vk, &proof, &tampered_signals);
    assert_eq!(res, false);
}
